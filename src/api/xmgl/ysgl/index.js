import fetch from 'utils/fetch';


// 获取验收申报信息
export function getYsxx(query) {
  return fetch({
    baseURL: process.env.BASE_API,
    url: '/api/XMGL/GetYsxx',
    method: 'get',
    params: query
  })
}
